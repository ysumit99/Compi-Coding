#include<bits/stdc++.h>

using namespace std;

int main(int argc, char const *argv[])
{
	int t;

	cin >>t;

	while(t--)
	{
		int n;

		cin >> n;

		int l,r;

		std::vector<long long int> v;

		for (int i = 0; i < n; ++i)
		{
			cin >> l >> r;
			v.push_back(make_pair(l,r));

		}
	}
	return 0;
}